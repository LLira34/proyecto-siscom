    private void validaCombo() {

        String message = "";
        if (cboTurno.getSelectedItem().equals("Selecciona Turno")) {
            message = "Elige una opcion del campo 'Turno'.\n";

        }
        if (cboCarrera.getSelectedItem().equals("Selecciona Carrera")) {
            message = "Elige una opcion del campo 'Carrera'.\n";

        }
        if (cboCurso.getSelectedItem().equals("Selecciona Curso")) {
            message = "Elige una opcion del campo 'Curso'.\n";

        }
        if (!message.isEmpty()) {
            JOptionPane.showMessageDialog(this, message, "Entrada al Sistema",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

