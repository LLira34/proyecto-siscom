    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String message = "";
        validaCombo();
        if (txtNombre.getText().trim().isEmpty()) {
            message = "Inserta datos el campo 'Nombre'.\n";
        }
        if (txtDescripcion.getText().trim().isEmpty()) {
            message = "Agrega una descripción \n en el campo correspontiente.\n";
        }
        if (txtCodigo.getText().trim().isEmpty()) {
            message = "Inserta datos el el campo 'Código'.\n";
        }
        if (!chkEstado.isSelected()) {
            message = "Verifique el estado se correcto.\n";
        }
        if (!message.isEmpty()) {
            JOptionPane.showMessageDialog(this, message, "Entrada al Sistema",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

